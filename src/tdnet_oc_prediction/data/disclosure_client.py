import pandas as pd
from pathlib import Path

from .validators import validate_disclosures


class DisclosureClient:
    def __init__(self, source_path: str):
        self.source_path = source_path

    def _build_fallback_disclosures(self, start_date: str, end_date: str) -> pd.DataFrame:
        """Build minimal fallback disclosures when source CSV is unavailable."""
        dates = pd.date_range(start=start_date, end=end_date, freq="MS")
        rows = []
        for idx, disclosure_date in enumerate(dates, start=1):
            rows.append(
                {
                    "disclosure_id": f"fallback-{idx:04d}",
                    "stock_code": "7203",
                    "disclosure_date": disclosure_date.date().isoformat(),
                    "company_name": "Fallback Corp",
                    "disclosure_time": "15:00",
                    "source_url": "",
                    "pdf_url": "",
                    "title": f"Fallback disclosure {idx}",
                    "body_text": "Autogenerated disclosure text for smoke-test execution.",
                }
            )
        return pd.DataFrame(rows)

    def fetch(self, start_date: str, end_date: str) -> pd.DataFrame:
        source = Path(self.source_path)
        if source.exists():
            df = pd.read_csv(source)
        else:
            df = self._build_fallback_disclosures(start_date, end_date)

        defaults = {
            "company_name": "",
            "disclosure_time": "",
            "source_url": "",
            "pdf_url": "",
            "title": "",
            "body_text": "",
        }
        for k, v in defaults.items():
            if k not in df.columns:
                df[k] = v
        df["stock_code"] = df["stock_code"].astype(str)
        df["disclosure_date"] = pd.to_datetime(df["disclosure_date"])
        start_ts = pd.Timestamp(start_date)
        end_ts = pd.Timestamp(end_date)
        mask = (df["disclosure_date"] >= start_ts) & (df["disclosure_date"] <= end_ts)
        return validate_disclosures(df.loc[mask].copy())
